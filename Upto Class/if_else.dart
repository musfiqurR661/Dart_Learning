void main() {
  int number = 10;

  // If-Else
  if (number > 5) {
    print('The number is greater than 5');
  } else {
    print('The number is less than or equal to 5');
  }

  // For Loop
  for (int i = 0; i < 5; i++) {
    print('i = $i');
  }

  // While Loop
  int j = 0;
  while (j < 5) {
    print('j = $j');
    j++;
  }
}
